type RenderEnv = BTreeMap<String, RenderExprCode>;

#[derive(Clone, Debug, PartialEq, Eq)]
enum RenderKind {
    Text,
    Number,
    Bool,
    Record,
    List(Box<RenderKind>),
}

impl RenderKind {
    fn list_item(&self) -> Option<&RenderKind> {
        match self {
            RenderKind::List(item) => Some(item),
            _ => None,
        }
    }
}

#[derive(Clone, Debug, PartialEq, Eq)]
struct RenderExprCode {
    kind: RenderKind,
    code: String,
}

impl RenderExprCode {
    fn text(&self) -> String {
        match &self.kind {
            RenderKind::Text => self.code.clone(),
            RenderKind::Number => format!("({}).to_string()", self.code),
            RenderKind::Bool => format!(
                "if {} {{ \"True\".to_owned() }} else {{ \"False\".to_owned() }}",
                self.code
            ),
            RenderKind::Record => "String::new()".to_owned(),
            RenderKind::List(_) => {
                format!("generated_join_display_values({}, \",\")", self.code)
            }
        }
    }

    fn render_text(&self) -> String {
        self.text()
    }

    fn number(&self) -> String {
        match &self.kind {
            RenderKind::Number => self.code.clone(),
            RenderKind::Text => format!("({}).parse::<i64>().unwrap_or_default()", self.code),
            RenderKind::Bool => format!("i64::from({})", self.code),
            RenderKind::List(_) => format!(
                "{{ let values = {}; generated_list_len(&values) }}",
                self.code
            ),
            RenderKind::Record => format!(
                "{{ let fields = {}; i64::try_from(fields.len()).unwrap_or(i64::MAX) }}",
                self.code
            ),
        }
    }

    fn bool(&self) -> String {
        match &self.kind {
            RenderKind::Bool => self.code.clone(),
            RenderKind::Text => format!(
                "{{ let text = {}; !text.is_empty() && text != \"False\" && text != \"false\" }}",
                self.code
            ),
            RenderKind::Number => format!("({}) != 0", self.code),
            RenderKind::List(_) => format!("!({}).is_empty()", self.code),
            RenderKind::Record => format!("!({}).is_empty()", self.code),
        }
    }
}

#[derive(Clone, Debug, PartialEq, Eq)]
struct RenderCollectionCode {
    kind: RenderKind,
    code: String,
}

impl RenderCollectionCode {
    fn into_text_collection(self) -> String {
        match self.kind {
            RenderKind::Text => self.code,
            RenderKind::Number => {
                format!(
                    "({}).map(|(identity, value)| (identity, value.to_string()))",
                    self.code
                )
            }
            RenderKind::Bool => format!(
                "({}).map(|(identity, value)| (identity, if value {{ \"True\".to_owned() }} else {{ \"False\".to_owned() }}))",
                self.code
            ),
            RenderKind::Record => {
                format!(
                    "({}).map(|(identity, _)| (identity, String::new()))",
                    self.code
                )
            }
            RenderKind::List(_) => format!(
                "({}).map(|(identity, values)| (identity, generated_join_display_values(values, \",\")))",
                self.code
            ),
        }
    }
}

fn value_collection_graph_code(
    graph: &boon_dd::DdRenderGraph,
    node: &boon_dd::NodeId,
    env: &RenderEnv,
) -> RenderCollectionCode {
    let node = render_graph_node(graph, node);
    match &node.operation {
        boon_dd::DdRenderGraphOperation::Source | boon_dd::DdRenderGraphOperation::Path(_) => {
            RenderCollectionCode {
                kind: RenderKind::Text,
                code: keyed_render_event_source_text(),
            }
        }
        boon_dd::DdRenderGraphOperation::Pipe { input, stage } => {
            let input = value_collection_graph_code(graph, input, env);
            stage_collection_graph_code(graph, stage, input, env)
        }
        boon_dd::DdRenderGraphOperation::Then { body } => {
            let value = body
                .last()
                .map(|value| value_graph_code(graph, value, env))
                .unwrap_or_else(|| unsupported_expr("empty THEN body"));
            RenderCollectionCode {
                kind: value.kind,
                code: keyed_render_event_value(&value.code),
            }
        }
        boon_dd::DdRenderGraphOperation::Hold { body, binder } => {
            let input = RenderCollectionCode {
                kind: RenderKind::Number,
                code: keyed_render_event_value("0_i64"),
            };
            hold_collection_graph_code(graph, input, body, binder, env)
        }
        boon_dd::DdRenderGraphOperation::Latest(_values) => latest_collection_code(),
        boon_dd::DdRenderGraphOperation::Match { arms, .. } => {
            let input = RenderCollectionCode {
                kind: RenderKind::Text,
                code: keyed_render_event_source_text(),
            };
            stage_match_graph_collection_code(graph, input, arms, env)
        }
        boon_dd::DdRenderGraphOperation::Missing | boon_dd::DdRenderGraphOperation::Skip => {
            let value = value_graph_code(graph, &node.node, env);
            RenderCollectionCode {
                kind: value.kind,
                code: keyed_render_event_value(&value.code),
            }
        }
        boon_dd::DdRenderGraphOperation::SourceAt { .. }
        | boon_dd::DdRenderGraphOperation::Link { .. } => {
            let value = value_graph_code(graph, &node.node, env);
            RenderCollectionCode {
                kind: value.kind,
                code: keyed_render_event_value(&value.code),
            }
        }
        boon_dd::DdRenderGraphOperation::Number(_)
        | boon_dd::DdRenderGraphOperation::Tag(_)
        | boon_dd::DdRenderGraphOperation::Text(_)
        | boon_dd::DdRenderGraphOperation::Record(_)
        | boon_dd::DdRenderGraphOperation::List(_)
        | boon_dd::DdRenderGraphOperation::Block(_)
        | boon_dd::DdRenderGraphOperation::Call { .. }
        | boon_dd::DdRenderGraphOperation::Constructor { .. }
        | boon_dd::DdRenderGraphOperation::FieldAccess { .. }
        | boon_dd::DdRenderGraphOperation::BinaryAdd { .. }
        | boon_dd::DdRenderGraphOperation::BinarySubtract { .. }
        | boon_dd::DdRenderGraphOperation::BinaryEqual { .. } => {
            let value = value_graph_code(graph, &node.node, env);
            RenderCollectionCode {
                kind: value.kind,
                code: keyed_render_event_value(&value.code),
            }
        }
    }
}

fn stage_collection_graph_code(
    graph: &boon_dd::DdRenderGraph,
    stage: &boon_dd::NodeId,
    input: RenderCollectionCode,
    env: &RenderEnv,
) -> RenderCollectionCode {
    let stage = render_graph_node(graph, stage);
    match &stage.operation {
        boon_dd::DdRenderGraphOperation::Call { callee, args } => {
            call_graph_collection_code(graph, callee, input, args, env)
        }
        boon_dd::DdRenderGraphOperation::SourceAt { .. }
        | boon_dd::DdRenderGraphOperation::Link { .. } => input.to_owned(),
        boon_dd::DdRenderGraphOperation::Then { body } => {
            let value = body
                .last()
                .map(|value| value_graph_code(graph, value, env))
                .unwrap_or_else(|| unsupported_expr("empty THEN body"));
            RenderCollectionCode {
                kind: value.kind,
                code: format!(
                    "({}).map(|(identity, _)| (identity, {}))",
                    input.code, value.code
                ),
            }
        }
        boon_dd::DdRenderGraphOperation::Hold { body, binder } => {
            hold_collection_graph_code(graph, input, body, binder, env)
        }
        boon_dd::DdRenderGraphOperation::Latest(_values) => latest_collection_code(),
        boon_dd::DdRenderGraphOperation::Match { arms, .. } => {
            stage_match_graph_collection_code(graph, input, arms, env)
        }
        _ => {
            let value = value_graph_code(graph, &stage.node, env);
            RenderCollectionCode {
                kind: value.kind,
                code: format!(
                    "({}).map(|(identity, _)| (identity, {}))",
                    input.code, value.code
                ),
            }
        }
    }
}

