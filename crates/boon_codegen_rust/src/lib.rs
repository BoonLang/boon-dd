fn number_arg_code(
    graph: &boon_dd::DdRenderGraph,
    input: Option<String>,
    args: &[boon_dd::DdRenderGraphArg],
    env: &BTreeMap<String, String>,
    name: &str,
) -> String {
    input
        .or_else(|| named_graph_arg_code(graph, args, name, env))
        .or_else(|| first_graph_arg_code(graph, args, env))
        .unwrap_or_else(|| unsupported_value_code(&format!("missing numeric argument {name}")))
}

fn bool_combinator_call(
    graph: &boon_dd::DdRenderGraph,
    input: Option<String>,
    args: &[boon_dd::DdRenderGraphArg],
    env: &BTreeMap<String, String>,
    op: &str,
    callee: &str,
) -> String {
    let left = input.unwrap_or_else(|| unsupported_value_code(&format!("{callee} missing input")));
    let right = named_graph_arg_code(graph, args, "that", env)
        .or_else(|| first_graph_arg_code(graph, args, env))
        .unwrap_or_else(|| unsupported_value_code(&format!("{callee} missing that")));
    format!(
        "DdValue::Tag((if ({}).truthy() {} ({}).truthy() {{ \"True\" }} else {{ \"False\" }}).to_owned())",
        left, op, right
    )
}

fn math_min_call(
    graph: &boon_dd::DdRenderGraph,
    input: Option<String>,
    args: &[boon_dd::DdRenderGraphArg],
    env: &BTreeMap<String, String>,
) -> String {
    let left = input
        .or_else(|| named_graph_arg_code(graph, args, "a", env))
        .or_else(|| named_graph_arg_code(graph, args, "left", env))
        .or_else(|| first_graph_arg_code(graph, args, env))
        .unwrap_or_else(|| unsupported_value_code("Math/min missing left"));
    let right = named_graph_arg_code(graph, args, "b", env)
        .or_else(|| named_graph_arg_code(graph, args, "right", env))
        .unwrap_or_else(|| unsupported_value_code("Math/min missing right"));
    format!(
        "DdValue::Number(std::cmp::min(({}).number(), ({}).number()))",
        left, right
    )
}

fn compare_number_call(
    graph: &boon_dd::DdRenderGraph,
    args: &[boon_dd::DdRenderGraphArg],
    env: &BTreeMap<String, String>,
    op: &str,
    callee: &str,
) -> String {
    let left = named_graph_arg_code(graph, args, "left", env)
        .unwrap_or_else(|| unsupported_value_code(&format!("{callee} missing left")));
    let right = named_graph_arg_code(graph, args, "right", env)
        .unwrap_or_else(|| unsupported_value_code(&format!("{callee} missing right")));
    format!(
        "DdValue::Tag((if ({}).number() {} ({}).number() {{ \"True\" }} else {{ \"False\" }}).to_owned())",
        left, op, right
    )
}

fn geometry_intersects_call(
    graph: &boon_dd::DdRenderGraph,
    args: &[boon_dd::DdRenderGraphArg],
    env: &BTreeMap<String, String>,
) -> String {
    let arg = |name: &str| {
        named_graph_arg_code(graph, args, name, env).unwrap_or_else(|| {
            unsupported_value_code(&format!("Geometry/intersects missing {name}"))
        })
    };
    let ax = arg("ax");
    let ay = arg("ay");
    let aw = arg("aw");
    let ah = arg("ah");
    let bx = arg("bx");
    let by = arg("by");
    let bw = arg("bw");
    let bh = arg("bh");
    format!(
        "{{ let ax = ({}).number(); let ay = ({}).number(); let aw = ({}).number(); let ah = ({}).number(); let bx = ({}).number(); let by = ({}).number(); let bw = ({}).number(); let bh = ({}).number(); DdValue::Tag((if ax < bx + bw && ax + aw > bx && ay < by + bh && ay + ah > by {{ \"True\" }} else {{ \"False\" }}).to_owned()) }}",
        ax, ay, aw, ah, bx, by, bw, bh
    )
}

fn element_render_text_call(
    graph: &boon_dd::DdRenderGraph,
    args: &[boon_dd::DdRenderGraphArg],
    env: &BTreeMap<String, String>,
    names: &[&str],
) -> Option<String> {
    names.iter().find_map(|name| {
        named_graph_arg_code(graph, args, name, env)
            .map(|value| format!("DdValue::Text(({}).render_text())", value))
    })
}

fn is_element_call(callee: &str) -> bool {
    let callee = canonical_library_call(callee);
    is_element_container_call(callee)
        || is_element_decorative_call(callee)
        || matches!(
            callee,
            "Element/button"
                | "Element/label"
                | "Element/link"
                | "Element/text"
                | "Element/text_input"
        )
}

fn is_element_container_call(callee: &str) -> bool {
    let callee = canonical_library_call(callee);
    matches!(
        callee,
        "Element/block"
            | "Element/checkbox"
            | "Element/container"
            | "Element/grid"
            | "Element/panel"
            | "Element/paragraph"
            | "Element/select"
            | "Element/slider"
            | "Element/stack"
            | "Element/stripe"
            | "Element/svg"
    )
}

fn is_element_decorative_call(callee: &str) -> bool {
    let callee = canonical_library_call(callee);
    matches!(callee, "Element/rect" | "Element/svg_circle")
}

fn first_graph_arg_code(
    graph: &boon_dd::DdRenderGraph,
    args: &[boon_dd::DdRenderGraphArg],
    env: &BTreeMap<String, String>,
) -> Option<String> {
    args.iter().find_map(|arg| match arg {
        boon_dd::DdRenderGraphArg::Positional(value) => Some(value_graph_code(graph, value, env)),
        boon_dd::DdRenderGraphArg::Named { .. } => None,
    })
}

fn named_graph_arg_code(
    graph: &boon_dd::DdRenderGraph,
    args: &[boon_dd::DdRenderGraphArg],
    name: &str,
    env: &BTreeMap<String, String>,
) -> Option<String> {
    named_graph_arg(args, name).map(|expr| value_graph_code(graph, expr, env))
}

fn named_graph_arg<'a>(
    args: &'a [boon_dd::DdRenderGraphArg],
    name: &str,
) -> Option<&'a boon_dd::NodeId> {
    args.iter().find_map(|arg| match arg {
        boon_dd::DdRenderGraphArg::Named {
            name: arg_name,
            value,
        } if arg_name == name => Some(value),
        _ => None,
    })
}

fn render_graph_node<'a>(
    graph: &'a boon_dd::DdRenderGraph,
    node: &boon_dd::NodeId,
) -> &'a boon_dd::DdRenderGraphNode {
    graph
        .nodes
        .iter()
        .find(|candidate| candidate.node == *node)
        .unwrap_or_else(|| panic!("missing DD render graph node {}", node.0))
}

fn latest_collection_code() -> String {
    "events.clone().filter(|(_sequence, event)| generated_event_matches_bound_source(event)).map(|(sequence, event)| ((), (sequence, generated_bound_source_value(&event)))).reduce(|_, inputs, output| {\n            if let Some(((sequence, value), _diff)) = inputs.iter().max_by_key(|((sequence, _), _)| *sequence) {\n                let _ = sequence;\n                output.push((value.clone(), 1));\n            }\n        }).map(|(_key, value)| value)".to_owned()
}

fn path_expr_code(path: &str, env: &BTreeMap<String, String>) -> String {
    let mut parts = path.split('.');
    let Some(root) = parts.next() else {
        return unsupported_value_code("empty path expression");
    };
    let Some(mut code) = env.get(root).cloned() else {
        return unsupported_value_code(&format!("unresolved generated render path root {root}"));
    };
    for part in parts {
        code = format!("({}).field({})", code, quote(part));
    }
    code
}

fn unsupported_value_code(reason: &str) -> String {
    format!(
        "panic!({})",
        quote(&format!("unsupported generated DD render value: {reason}"))
    )
}

fn quote(value: &str) -> String {
    format!("{value:?}")
}

