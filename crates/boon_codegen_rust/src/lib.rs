fn number_arg_code(
    graph: &boon_dd::DdRenderGraph,
    input: Option<RenderExprCode>,
    args: &[boon_dd::DdRenderGraphArg],
    env: &RenderEnv,
    name: &str,
) -> RenderExprCode {
    input
        .or_else(|| named_graph_arg_code(graph, args, name, env))
        .or_else(|| first_graph_arg_code(graph, args, env))
        .unwrap_or_else(|| unsupported_expr(&format!("missing numeric argument {name}")))
}

fn bool_combinator_call(
    graph: &boon_dd::DdRenderGraph,
    input: Option<RenderExprCode>,
    args: &[boon_dd::DdRenderGraphArg],
    env: &RenderEnv,
    op: &str,
    callee: &str,
) -> RenderExprCode {
    let left = input.unwrap_or_else(|| unsupported_expr(&format!("{callee} missing input")));
    let right = named_graph_arg_code(graph, args, "that", env)
        .or_else(|| first_graph_arg_code(graph, args, env))
        .unwrap_or_else(|| unsupported_expr(&format!("{callee} missing that")));
    RenderExprCode {
        kind: RenderKind::Bool,
        code: format!("({}) {} ({})", left.bool(), op, right.bool()),
    }
}

fn math_min_call(
    graph: &boon_dd::DdRenderGraph,
    input: Option<RenderExprCode>,
    args: &[boon_dd::DdRenderGraphArg],
    env: &RenderEnv,
) -> RenderExprCode {
    let left = input
        .or_else(|| named_graph_arg_code(graph, args, "a", env))
        .or_else(|| named_graph_arg_code(graph, args, "left", env))
        .or_else(|| first_graph_arg_code(graph, args, env))
        .unwrap_or_else(|| unsupported_expr("Math/min missing left"));
    let right = named_graph_arg_code(graph, args, "b", env)
        .or_else(|| named_graph_arg_code(graph, args, "right", env))
        .unwrap_or_else(|| unsupported_expr("Math/min missing right"));
    RenderExprCode {
        kind: RenderKind::Number,
        code: format!("std::cmp::min({}, {})", left.number(), right.number()),
    }
}

fn compare_number_call(
    graph: &boon_dd::DdRenderGraph,
    args: &[boon_dd::DdRenderGraphArg],
    env: &RenderEnv,
    op: &str,
    callee: &str,
) -> RenderExprCode {
    let left = named_graph_arg_code(graph, args, "left", env)
        .unwrap_or_else(|| unsupported_expr(&format!("{callee} missing left")));
    let right = named_graph_arg_code(graph, args, "right", env)
        .unwrap_or_else(|| unsupported_expr(&format!("{callee} missing right")));
    RenderExprCode {
        kind: RenderKind::Bool,
        code: format!("({}) {} ({})", left.number(), op, right.number()),
    }
}

fn geometry_intersects_call(
    graph: &boon_dd::DdRenderGraph,
    args: &[boon_dd::DdRenderGraphArg],
    env: &RenderEnv,
) -> RenderExprCode {
    let arg = |name: &str| {
        named_graph_arg_code(graph, args, name, env)
            .unwrap_or_else(|| unsupported_expr(&format!("Geometry/intersects missing {name}")))
    };
    let ax = arg("ax");
    let ay = arg("ay");
    let aw = arg("aw");
    let ah = arg("ah");
    let bx = arg("bx");
    let by = arg("by");
    let bw = arg("bw");
    let bh = arg("bh");
    RenderExprCode {
        kind: RenderKind::Bool,
        code: format!(
            "{{ let ax = {}; let ay = {}; let aw = {}; let ah = {}; let bx = {}; let by = {}; let bw = {}; let bh = {}; ax < bx + bw && ax + aw > bx && ay < by + bh && ay + ah > by }}",
            ax.number(),
            ay.number(),
            aw.number(),
            ah.number(),
            bx.number(),
            by.number(),
            bw.number(),
            bh.number()
        ),
    }
}

fn element_render_text_call(
    graph: &boon_dd::DdRenderGraph,
    args: &[boon_dd::DdRenderGraphArg],
    env: &RenderEnv,
    names: &[&str],
) -> Option<RenderExprCode> {
    names.iter().find_map(|name| {
        named_graph_arg_code(graph, args, name, env).map(|value| RenderExprCode {
            kind: RenderKind::Text,
            code: value.render_text(),
        })
    })
}

fn is_element_call(callee: &str) -> bool {
    let callee = canonical_library_call(callee);
    is_element_container_call(callee)
        || is_element_decorative_call(callee)
        || matches!(
            callee,
            "Element/button"
                | "Element/label"
                | "Element/link"
                | "Element/text"
                | "Element/text_input"
        )
}

fn is_element_container_call(callee: &str) -> bool {
    let callee = canonical_library_call(callee);
    matches!(
        callee,
        "Element/block"
            | "Element/checkbox"
            | "Element/container"
            | "Element/grid"
            | "Element/panel"
            | "Element/paragraph"
            | "Element/select"
            | "Element/slider"
            | "Element/stack"
            | "Element/stripe"
            | "Element/svg"
    )
}

fn is_element_decorative_call(callee: &str) -> bool {
    let callee = canonical_library_call(callee);
    matches!(callee, "Element/rect" | "Element/svg_circle")
}

fn first_graph_arg_code(
    graph: &boon_dd::DdRenderGraph,
    args: &[boon_dd::DdRenderGraphArg],
    env: &RenderEnv,
) -> Option<RenderExprCode> {
    args.iter().find_map(|arg| match arg {
        boon_dd::DdRenderGraphArg::Positional(value) => Some(value_graph_code(graph, value, env)),
        boon_dd::DdRenderGraphArg::Named { .. } => None,
    })
}

fn named_graph_arg_code(
    graph: &boon_dd::DdRenderGraph,
    args: &[boon_dd::DdRenderGraphArg],
    name: &str,
    env: &RenderEnv,
) -> Option<RenderExprCode> {
    named_graph_arg(args, name).map(|expr| value_graph_code(graph, expr, env))
}

fn named_graph_arg<'a>(
    args: &'a [boon_dd::DdRenderGraphArg],
    name: &str,
) -> Option<&'a boon_dd::NodeId> {
    args.iter().find_map(|arg| match arg {
        boon_dd::DdRenderGraphArg::Named {
            name: arg_name,
            value,
        } if arg_name == name => Some(value),
        _ => None,
    })
}

fn render_graph_node<'a>(
    graph: &'a boon_dd::DdRenderGraph,
    node: &boon_dd::NodeId,
) -> &'a boon_dd::DdRenderGraphNode {
    graph
        .nodes
        .iter()
        .find(|candidate| candidate.node == *node)
        .unwrap_or_else(|| panic!("missing DD render graph node {}", node.0))
}

fn latest_collection_code() -> RenderCollectionCode {
    RenderCollectionCode {
        kind: RenderKind::Text,
        code: "render_events.clone().flat_map(|(sequence, event)| generated_event_identity(&event).map(|identity| (identity, (sequence, generated_source_text(&event))))).reduce(|_, inputs, output| {\n            if let Some(((sequence, value), _diff)) = inputs.iter().max_by_key(|((sequence, _), _)| *sequence) {\n                let _ = sequence;\n                output.push((value.clone(), 1));\n            }\n        }).map(|(identity, value)| (identity, value))".to_owned(),
    }
}

fn path_expr_code(path: &str, env: &RenderEnv) -> RenderExprCode {
    let mut parts = path.split('.');
    let Some(root) = parts.next() else {
        return unsupported_expr("empty path expression");
    };
    let Some(mut value) = env.get(root).cloned() else {
        return unsupported_expr(&format!("unresolved generated render path root {root}"));
    };
    for part in parts {
        value = RenderExprCode {
            kind: RenderKind::Text,
            code: format!(
                "({}).get({}).cloned().unwrap_or_default()",
                value.code,
                quote(part)
            ),
        };
    }
    value
}

fn empty_text_expr() -> RenderExprCode {
    RenderExprCode {
        kind: RenderKind::Text,
        code: "String::new()".to_owned(),
    }
}

fn default_expr_for_kind(kind: &RenderKind) -> RenderExprCode {
    match kind {
        RenderKind::Text => empty_text_expr(),
        RenderKind::Number => RenderExprCode {
            kind: RenderKind::Number,
            code: "0_i64".to_owned(),
        },
        RenderKind::Bool => RenderExprCode {
            kind: RenderKind::Bool,
            code: "false".to_owned(),
        },
        RenderKind::Record => RenderExprCode {
            kind: RenderKind::Record,
            code: "std::collections::BTreeMap::<String, String>::new()".to_owned(),
        },
        RenderKind::List(item) => RenderExprCode {
            kind: RenderKind::List(item.clone()),
            code: "Vec::new()".to_owned(),
        },
    }
}

fn unsupported_expr(reason: &str) -> RenderExprCode {
    RenderExprCode {
        kind: RenderKind::Text,
        code: format!(
            "panic!({})",
            quote(&format!("unsupported generated DD render value: {reason}"))
        ),
    }
}

fn quote(value: &str) -> String {
    format!("{value:?}")
}

